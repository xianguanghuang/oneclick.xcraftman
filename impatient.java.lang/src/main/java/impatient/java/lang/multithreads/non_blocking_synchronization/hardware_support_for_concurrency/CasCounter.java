package impatient.java.lang.multithreads.non_blocking_synchronization.hardware_support_for_concurrency;

public class CasCounter {
    private SimulatedCAS value;

    public int getValue() {
        return value.get();
    }

    public int increment() {
        int v;
        do {
            v = value.get();
        } while (v != value.compareAndSwap(v, v + 1));
        return v + 1;

    }

}
